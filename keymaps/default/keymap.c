layer_state_t layer_state_set_user(layer_state_t state) {
    return update_tri_layer_state(state, _SYMB, _NAV, _ADJUST);
}

void oled_render_layer_state(void) {
    // Host Keyboard Layer Status
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write_ln_P(PSTR("QWE"), false);
            oled_write_ln_P(PSTR("RTZ"), false);
            break;
        case _SYMB:
            oled_write_ln_P(PSTR("SYM"), false);
            oled_write_ln_P(PSTR("BOL"), false);
            break;
        case _NAV:
            oled_write_ln_P(PSTR("NAV"), false);
            oled_write_ln_P(PSTR("IGA"), false);
            break;
        case _NAV2:
            oled_write_ln_P(PSTR("NAV"), false);
            oled_write_ln_P(PSTR("IGA"), false);
            break;
        case _ADJUST:
            oled_write_ln_P(PSTR("ADJ"), false);
            oled_write_ln_P(PSTR("UST"), false);
            break;
        default:
            oled_write_ln_P(PSTR("XXX"), false);
    }

    // Host Keyboard LED Status
    led_t led_state = host_keyboard_led_state();
    oled_write_ln_P(PSTR(""), false);
    oled_write_ln_P(led_state.caps_lock ? PSTR("CAPS LOCK") : PSTR("         "), false);
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    if (is_keyboard_master()) {
        return OLED_ROTATION_270 ;
    }
    return rotation;
}

bool oled_task_user(void) {
    if (is_keyboard_left()) {
        oled_render_layer_state();
    } else {
        oled_render_logo();
    }
    return false;
}

